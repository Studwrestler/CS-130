package lab3;

public class AutoExplicit {

	public static void main(String[] args) 	{
		int i =30;
		System.out.println("The integer number is "+i);
		double d =i;
		System.out.println("The double number is "+d);
		float f =(float)d;
		System.out.println("The float point number is "+f);
		int j =(int)f;
		System.out.println("The integer number is "+j);

	}

}
